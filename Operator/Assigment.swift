/*Operator
Deskripsi
Contoh (A adalah 2 dan B adalah 4)
=
Operator penugasan sederhana, menetapkan nilai operan sisi kanan ke sisi kiri.
C = A + B, setara dengan C = A + B. 
+=
Operator penugasan dan penambahan, menambahkan operan kanan ke operan  kiri dan menetapkannya hasilnya ke operan kiri.
C += A, setara dengan C = C + A.
-=
Operator penugasan dan pengurangan, mengurangi operan kanan dari operan kiri dan menetapkan hasilnya ke operan kiri.
C -= A, setara dengan C = C - A.
*=
Operator penugasan dan perkalian, mengalikan operan kanan dengan operan kiri dan menetapkan hasilnya ke operan kiri.
C *= A, setara dengan C = C * A.
/=
Operator penugasan dan pembagian, membagi operan kanan dengan operan kiri dan menetapkan hasilnya ke opran kiri.
C /= A, setara dengan C = C / A.
%=
Operator penugasan dan modulus, mengambil nilai modulus dari dua operan dan menentapkan hasilnya ke operan kiri.
C %= A, setara dengan C = C % A.
<<=
Operator penugasan dan bitwise pergeseran ke kiri.
C <<= 2, setara dengan C = C << 2.
>>=
Operator penugasan dan bitwise pergeseran ke kanan.
C >>= 2, setara dengan C = C >> 2.
&=
Operator penugasan dan bitwise AND.
C &= 2, setara dengan C = C & 2.
^=
Operator penugasan dan bitwise exclusive OR.
C ^= 2, setara dengan C = C ^ 2.
|=
Operator penugasan dan bitwise inclusive OR.
C |= 2, setara dengan C = C | 2.
